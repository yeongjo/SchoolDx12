void CScene::BuildObjects(ID3D12Device *pd3dDevice, ID3D12GraphicsCommandList *pd3dCommandList, void *pContext) {
	m_pd3dGraphicsRootSignature = CreateGraphicsRootSignature(pd3dDevice);

	BuildDefaultLightsAndMaterials();

	m_pSkyBox = new CSkyBox(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature);

	XMFLOAT3 xmf3Scale(8.0f, 2.0f, 8.0f);
	XMFLOAT4 xmf4Color(0.0f, 0.5f, 0.0f, 0.0f);
#ifdef _WITH_TERRAIN_PARTITION
	m_pTerrain = new CHeightMapTerrain(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature, _T("Image/HeightMap.raw"), 257, 257, 17, 17, xmf3Scale, xmf4Color);
#else
	auto pTerrain = new CHeightMapTerrain(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature, _T("Image/HeightMap.raw"), 257, 257, 257, 257, xmf3Scale, xmf4Color);
#endif
	auto wayPoints = new WayPointHandler;
	auto mapSize = XMFLOAT3(256 * 8, 200 * 2, 256 * 8);
	wayPoints->wayPoints.push_back(Vector3::Mul(XMFLOAT3(0.109375, 1, 1 - 0.4765625), mapSize));
	wayPoints->wayPoints.push_back(Vector3::Mul(XMFLOAT3(0.3125, 1, 1 - 0.5234375), mapSize));
	wayPoints->wayPoints.push_back(Vector3::Mul(XMFLOAT3(0.3828125, 1, 1 - 0.5859375), mapSize));
	wayPoints->wayPoints.push_back(Vector3::Mul(XMFLOAT3(0.38671875, 1, 1 - 0.765625), mapSize));
	wayPoints->wayPoints.push_back(Vector3::Mul(XMFLOAT3(0.63671875, 1, 1 - 0.921875), mapSize));
	wayPoints->wayPoints.push_back(Vector3::Mul(XMFLOAT3(0.8, 1, 1 - 0.921875), mapSize));
	_wayPointHandler = wayPoints;

	

	auto pObjectsShader = new CObjectsShader();
	pObjectsShader->CreateShader(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature);
	pObjectsShader->CreateCbvSrvDescriptorHeaps(pd3dDevice, 0, 17 + 50); //SuperCobra(17), Gunship(2)
	CGameObject *pSuperCobraModel = new CSuperCobraObject(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature);
	pSuperCobraModel->SetChild(CGameObject::LoadGeometryFromFile(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature, ASSETPATH"/Model/SuperCobra.bin", pObjectsShader));
	pSuperCobraModel->PrepareAnimate();
	CGameObject* pGunshipModel = new CGunshipObject(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature);
	pGunshipModel->SetChild(CGameObject::LoadGeometryFromFile(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature, ASSETPATH"/Model/Gunship.bin", pObjectsShader));
	pGunshipModel->PrepareAnimate();

	pTerrainWater = new CTerrainWater(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature, 257 * xmf3Scale.x, 257 * xmf3Scale.z);
	pTerrainWater->SetPosition(+(257 * xmf3Scale.x * 0.5f), 55.0f, +(257 * xmf3Scale.z * 0.5f));
	auto pTexturedShader = new CTexturedShader();

	auto t = new ObjectSpawner();
	t->targetShader = pObjectsShader;
	t->baseObjs.push_back(pSuperCobraModel);
	t->baseObjs.push_back(pGunshipModel);
	t->bulletShader = pTexturedShader;
	m_ppGameObjects.push_back(t);
	m_ppGameObjects.push_back(pTerrain);

	m_ppShaders.resize(3);
	
	pObjectsShader->BuildObjects(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature);
	m_ppShaders[0] = pObjectsShader;

	auto pObjectShader = new CBillboardObjectsShader();
	pObjectShader->m_d3dTopologyType = D3D12_PRIMITIVE_TOPOLOGY_TYPE_POINT;
	pObjectShader->CreateShader(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature);
	pObjectShader->BuildObjects(pd3dDevice, pd3dCommandList, pTerrain);
	m_ppShaders[1] = pObjectShader;
	
	auto pParticleShader = new CBillboardParticleShader();
	pParticleShader->m_d3dTopologyType = D3D12_PRIMITIVE_TOPOLOGY_TYPE_POINT;
	pParticleShader->CreateShader(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature);
	pParticleShader->BuildObjects(pd3dDevice, pd3dCommandList, wayPoints);
	m_ppShaders[2] = pParticleShader;

	pTexturedShader->CreateShader(pd3dDevice, pd3dCommandList, m_pd3dGraphicsRootSignature);
	t->InitBullet(pd3dDevice, pd3dCommandList);
	
	m_ppShaders.push_back(pTexturedShader);

	CreateShaderVariables(pd3dDevice, pd3dCommandList);
}

void CScene::AnimateObjects(float fTimeElapsed) {
	for (int i = 0; i < m_ppGameObjects.size(); i++) 
		if (m_ppGameObjects[i])
			m_ppGameObjects[i]->Animate(fTimeElapsed, nullptr);
	for (int i = 0; i < m_ppGameObjects.size(); i++)
		if (m_ppGameObjects[i])
			m_ppGameObjects[i]->UpdateTransform(nullptr);

	for (int i = 0; i < m_ppShaders.size(); i++)
		if (m_ppShaders[i])
			m_ppShaders[i]->AnimateObjects(fTimeElapsed);

	if (m_pLights) {
		m_pLights[0].m_xmf3Position = m_pPlayer->GetPosition();
		m_pLights[1].m_xmf3Position = m_pPlayer->GetPosition();
		m_pLights[1].m_xmf3Direction = m_pPlayer->GetLookVector();
	}
}
